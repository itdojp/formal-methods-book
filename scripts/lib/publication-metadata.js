function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function resolveWithinRoot(repoRoot, relativePath, label) {
  if (!isNonEmptyString(relativePath) || path.isAbsolute(relativePath)) {
    throw new Error(`${label}: safe repository-relative path required`);
  }
  const resolvedRoot = path.resolve(repoRoot);
  const resolvedPath = path.resolve(resolvedRoot, relativePath);
  if (resolvedPath === resolvedRoot || !resolvedPath.startsWith(`${resolvedRoot}${path.sep}`)) {
    throw new Error(`${label}: path escapes repository root`);
  }
  return resolvedPath;
}

function loadPublicationModel(repoRoot) {
  const manifest = readJson(path.join(repoRoot, 'book-config.json'));
  const publicationPath = manifest.publication?.config || 'publication-config.json';
  const publication = readJson(resolveWithinRoot(repoRoot, publicationPath, 'book-config.json publication.config'));
  const editions = {};
  for (const [locale, edition] of Object.entries(manifest.editions || {})) {
    editions[locale] = readJson(resolveWithinRoot(repoRoot, edition.config, `book-config.json editions.${locale}.config`));
  }
  return { manifest, publication, editions };
}

function isNonEmptyString(value) {
  return typeof value === 'string' && value.trim().length > 0;
}

function isSafePathSegment(value) {
  return isNonEmptyString(value)
    && value === value.trim()
    && value !== '.'
    && value !== '..'
    && !/[\\/\0]/.test(value);
}

function duplicateValues(values) {
  const seen = new Set();
  const duplicates = new Set();
  for (const value of values) {
    if (seen.has(value)) duplicates.add(value);
    seen.add(value);
  }
  return [...duplicates];
}

function repositoryIdentity(repositoryUrl) {
  try {
    const parsed = new URL(repositoryUrl);
    const [owner, repository] = parsed.pathname
      .replace(/\.git$/, '')
      .split('/')
      .filter(Boolean);
    if (parsed.hostname !== 'github.com' || !owner || !repository) return null;
    return { owner, repository };
  } catch {
    return null;
  }
}

function canonicalContentPath(basePath, section, id) {
  const prefix = basePath === '/' ? '' : basePath.replace(/\/$/, '');
  return `${prefix}/${section}/${id}/`;
}

function canonicalSpecialPath(basePath, id) {
  const prefix = basePath === '/' ? '' : basePath.replace(/\/$/, '');
  return `${prefix}/${id}/`;
}

function resolveSourcePage(config, relativePath) {
  const posixPath = relativePath.split(path.sep).join('/');
  if (posixPath === 'index.md') {
    return {
      section: 'root',
      id: null,
      metadata: { title: config.title, description: config.description },
      outputSegments: ['index.md'],
    };
  }

  const contentMatch = posixPath.match(/^(chapters|appendices)\/([^/]+)\.md$/);
  if (contentMatch) {
    const [, section, id] = contentMatch;
    const metadata = config.structure?.[section]?.find((entry) => entry.id === id);
    return metadata ? { section, id, metadata, outputSegments: [section, id, 'index.md'] } : null;
  }

  const specialMatch = posixPath.match(/^([^/]+)\/index\.md$/);
  if (specialMatch) {
    const id = specialMatch[1];
    const metadata = config.structure?.specialPages?.find((entry) => entry.id === id);
    return metadata ? { section: 'specialPages', id, metadata, outputSegments: [id, 'index.md'] } : null;
  }

  return null;
}

function validateOrderedEntries({ locale, section, entries, basePath, errors }) {
  if (!Array.isArray(entries) || entries.length === 0) {
    errors.push(`${locale} structure.${section}: non-empty array required`);
    return;
  }

  const ids = entries.map((entry) => entry?.id);
  const paths = entries.map((entry) => entry?.path);
  const orders = entries.map((entry) => entry?.order);
  for (const duplicate of duplicateValues(ids)) {
    errors.push(`${locale} structure.${section}: duplicate id ${JSON.stringify(duplicate)}`);
  }
  for (const duplicate of duplicateValues(paths)) {
    errors.push(`${locale} structure.${section}: duplicate path ${JSON.stringify(duplicate)}`);
  }
  for (const duplicate of duplicateValues(orders)) {
    errors.push(`${locale} structure.${section}: duplicate order ${JSON.stringify(duplicate)}`);
  }

  for (const [index, entry] of entries.entries()) {
    const label = `${locale} structure.${section}[${index}]`;
    for (const key of ['id', 'title', 'description', 'path']) {
      if (!isNonEmptyString(entry?.[key])) errors.push(`${label}.${key}: non-empty string required`);
    }
    if (isNonEmptyString(entry?.id) && !isSafePathSegment(entry.id)) {
      errors.push(`${label}.id: safe path segment required`);
    }
    if (!Number.isInteger(entry?.order) || entry.order < 0) {
      errors.push(`${label}.order: non-negative integer required`);
    }
    if (index > 0 && Number.isInteger(entry?.order) && entry.order <= entries[index - 1]?.order) {
      errors.push(`${label}.order: entries must be strictly increasing`);
    }
    if (isNonEmptyString(entry?.id)) {
      const expectedPath = canonicalContentPath(basePath, section, entry.id);
      if (entry.path !== expectedPath) {
        errors.push(`${label}.path: expected ${JSON.stringify(expectedPath)}, got ${JSON.stringify(entry.path)}`);
      }
    }
  }
}

function validatePublicationModel(model) {
  const errors = [];
  const { manifest, publication, editions } = model;
  const editionEntries = Object.entries(manifest.editions || {});
  const localeIds = editionEntries.map(([locale]) => locale);

  if (manifest.manifestVersion !== '2.0') errors.push('book-config.json manifestVersion must be 2.0');
  if (publication.schemaVersion !== '1.0') errors.push('publication-config.json schemaVersion must be 1.0');
  if (!isNonEmptyString(manifest.project?.id)) errors.push('book-config.json project.id is required');
  if (manifest.publication?.config !== 'publication-config.json') {
    errors.push('book-config.json publication.config must be publication-config.json');
  }
  if (manifest.translation?.statusManifest !== 'translation-status.json') {
    errors.push('book-config.json translation.statusManifest must be translation-status.json');
  }
  if (!localeIds.includes(manifest.project?.defaultEdition)) {
    errors.push('book-config.json project.defaultEdition must name an edition');
  }
  if (localeIds.length !== 2) errors.push('book-config.json must define exactly two editions');
  if (!repositoryIdentity(manifest.repository?.url)) {
    errors.push('book-config.json repository.url must be an owner/repository GitHub URL');
  }
  for (const key of ['organization', 'markdown', 'highlighter', 'permalink']) {
    if (!isNonEmptyString(publication.jekyll?.[key])) {
      errors.push(`publication-config.json jekyll.${key}: non-empty string required`);
    }
  }
  for (const key of ['plugins', 'exclude', 'defaults']) {
    if (!Array.isArray(publication.jekyll?.[key])) {
      errors.push(`publication-config.json jekyll.${key}: array required`);
    }
  }
  for (const key of ['performance', 'navigation', 'typography', 'layout']) {
    if (!publication.mobile?.[key] || typeof publication.mobile[key] !== 'object' || Array.isArray(publication.mobile[key])) {
      errors.push(`publication-config.json mobile.${key}: object required`);
    }
  }

  for (const [locale, manifestEdition] of editionEntries) {
    const config = editions[locale];
    const label = `book-config.${locale}.json`;
    if (!config) {
      errors.push(`${label}: config not loaded`);
      continue;
    }
    const expectedManifestPaths = locale === manifest.project?.defaultEdition
      ? {
        config: `book-config.${locale}.json`,
        mobileConfig: `mobile-config.${locale}.json`,
        sourceRoot: `src/${locale}`,
        publishRoot: 'docs',
        canonicalBasePath: '/',
      }
      : {
        config: `book-config.${locale}.json`,
        mobileConfig: `mobile-config.${locale}.json`,
        sourceRoot: `src/${locale}`,
        publishRoot: `docs/${locale}`,
        canonicalBasePath: `/${locale}/`,
      };
    for (const [key, expected] of Object.entries(expectedManifestPaths)) {
      if (manifestEdition[key] !== expected) {
        errors.push(`book-config.json editions.${locale}.${key}: expected ${JSON.stringify(expected)}, got ${JSON.stringify(manifestEdition[key])}`);
      }
    }
    const isDefaultEdition = locale === manifest.project?.defaultEdition;
    if (manifestEdition.sourceOfTruth !== isDefaultEdition) {
      errors.push(`book-config.json editions.${locale}.sourceOfTruth must be ${isDefaultEdition}`);
    }
    if (config.edition?.sourceOfTruth !== isDefaultEdition) {
      errors.push(`${label} edition.sourceOfTruth must be ${isDefaultEdition}`);
    }
    const expectedTranslationOf = isDefaultEdition ? undefined : manifest.project?.defaultEdition;
    if (manifestEdition.translationOf !== expectedTranslationOf) {
      errors.push(`book-config.json editions.${locale}.translationOf must be ${JSON.stringify(expectedTranslationOf)}`);
    }
    if (config.edition?.translationOf !== expectedTranslationOf) {
      errors.push(`${label} edition.translationOf must be ${JSON.stringify(expectedTranslationOf)}`);
    }
    for (const key of ['title', 'subtitle', 'description', 'author', 'version', 'language']) {
      if (!isNonEmptyString(config[key])) errors.push(`${label} ${key}: non-empty string required`);
    }
    if (config.version !== manifest.project?.version) errors.push(`${label} version must match project.version`);
    if (config.language !== manifestEdition.language) errors.push(`${label} language must match manifest edition`);
    if (config.edition?.id !== locale) errors.push(`${label} edition.id must equal ${locale}`);
    if (!isNonEmptyString(config.edition?.locale)) errors.push(`${label} edition.locale: non-empty string required`);
    if (config.edition?.label !== manifestEdition.label) errors.push(`${label} edition.label must match manifest edition`);
    if (config.edition?.sourceRoot !== manifestEdition.sourceRoot) errors.push(`${label} edition.sourceRoot drift`);
    if (config.edition?.publishRoot !== manifestEdition.publishRoot) errors.push(`${label} edition.publishRoot drift`);
    if (config.edition?.mobileConfig !== manifestEdition.mobileConfig) errors.push(`${label} edition.mobileConfig drift`);

    const parts = config.structure?.parts;
    if (!Array.isArray(parts) || parts.length === 0) {
      errors.push(`${label} structure.parts: non-empty array required`);
    } else {
      for (const duplicate of duplicateValues(parts.map((entry) => entry?.id))) {
        errors.push(`${label} structure.parts: duplicate id ${JSON.stringify(duplicate)}`);
      }
      for (const duplicate of duplicateValues(parts.map((entry) => entry?.order))) {
        errors.push(`${label} structure.parts: duplicate order ${JSON.stringify(duplicate)}`);
      }
      const partIds = new Set(parts.map((entry) => entry?.id));
      for (const [index, part] of parts.entries()) {
        if (!isNonEmptyString(part?.id)) errors.push(`${label} structure.parts[${index}].id is required`);
        if (!isNonEmptyString(part?.title)) errors.push(`${label} structure.parts[${index}].title is required`);
        if (part?.description !== undefined && !isNonEmptyString(part.description)) {
          errors.push(`${label} structure.parts[${index}].description must be a non-empty string when present`);
        }
        if (part?.order !== index + 1) errors.push(`${label} structure.parts[${index}].order must be ${index + 1}`);
      }
      for (const [index, chapter] of (config.structure?.chapters || []).entries()) {
        if (!partIds.has(chapter?.part)) {
          errors.push(`${label} structure.chapters[${index}].part must reference structure.parts`);
        }
      }
    }

    validateOrderedEntries({
      locale,
      section: 'chapters',
      entries: config.structure?.chapters,
      basePath: manifestEdition.canonicalBasePath,
      errors,
    });
    validateOrderedEntries({
      locale,
      section: 'appendices',
      entries: config.structure?.appendices,
      basePath: manifestEdition.canonicalBasePath,
      errors,
    });

    const specialPages = config.structure?.specialPages;
    if (!Array.isArray(specialPages) || specialPages.length === 0) {
      errors.push(`${label} structure.specialPages: non-empty array required`);
    } else {
      for (const duplicate of duplicateValues(specialPages.map((entry) => entry?.id))) {
        errors.push(`${label} structure.specialPages: duplicate id ${JSON.stringify(duplicate)}`);
      }
      for (const page of specialPages) {
        if (!isNonEmptyString(page?.id) || !isNonEmptyString(page?.title) || !isNonEmptyString(page?.path)) {
          errors.push(`${label} structure.specialPages entries require id, title, and path`);
        }
        if (isNonEmptyString(page?.id) && !isSafePathSegment(page.id)) {
          errors.push(`${label} structure.specialPages id ${JSON.stringify(page.id)}: safe path segment required`);
        }
        if (!['introduction', 'additional', 'afterword'].includes(page?.section)) {
          errors.push(`${label} structure.specialPages ${page?.id}: unsupported section ${JSON.stringify(page?.section)}`);
        }
        if (!Number.isInteger(page?.order) || page.order < 0) {
          errors.push(`${label} structure.specialPages ${page?.id}: non-negative integer order required`);
        }
        if (isNonEmptyString(page?.id)) {
          const expectedPath = canonicalSpecialPath(manifestEdition.canonicalBasePath, page.id);
          if (page.path !== expectedPath) {
            errors.push(`${label} structure.specialPages ${page.id}.path: expected ${JSON.stringify(expectedPath)}, got ${JSON.stringify(page.path)}`);
          }
        }
      }
    }

    const navigationEntries = [
      ...(config.structure?.specialPages || []),
      ...(config.structure?.chapters || []),
      ...(config.structure?.appendices || []),
    ];
    const expectedOrders = Array.from({ length: navigationEntries.length }, (_, index) => index);
    const actualOrders = navigationEntries.map((entry) => entry.order).sort((left, right) => left - right);
    if (JSON.stringify(actualOrders) !== JSON.stringify(expectedOrders)) {
      errors.push(`${label} navigation order must be contiguous 0..${navigationEntries.length - 1}`);
    }
    if (duplicateValues(navigationEntries.map((entry) => entry.path)).length > 0) {
      errors.push(`${label} navigation paths must be unique`);
    }

    if (!isNonEmptyString(config.ui?.languageName) || !isNonEmptyString(config.ui?.alternateLanguageName)) {
      errors.push(`${label} ui language names are required`);
    }
    if (!isNonEmptyString(config.ui?.footerLicense)) errors.push(`${label} ui.footerLicense is required`);
    for (const key of REQUIRED_UI_LABELS) {
      if (!isNonEmptyString(config.ui?.labels?.[key])) errors.push(`${label} ui.labels.${key} is required`);
    }
  }

  const referenceLocale = manifest.project?.defaultEdition;
  const reference = editions[referenceLocale];
  if (reference) {
    for (const locale of localeIds.filter((candidate) => candidate !== referenceLocale)) {
      const candidate = editions[locale];
      if (!candidate) continue;
      for (const section of ['parts', 'chapters', 'appendices']) {
        const referenceEntries = reference.structure?.[section] || [];
        const candidateEntries = candidate.structure?.[section] || [];
        const referenceIds = referenceEntries.map((entry) => entry.id);
        const candidateIds = candidateEntries.map((entry) => entry.id);
        if (JSON.stringify(candidateIds) !== JSON.stringify(referenceIds)) {
          errors.push(`${locale} structure.${section} ids must match ${referenceLocale} 1:1`);
        }
        const referenceOrders = referenceEntries.map((entry) => entry.order);
        const candidateOrders = candidateEntries.map((entry) => entry.order);
        if (JSON.stringify(candidateOrders) !== JSON.stringify(referenceOrders)) {
          errors.push(`${locale} structure.${section} orders must match ${referenceLocale} 1:1`);
        }
      }
      const referenceParts = (reference.structure?.chapters || []).map((entry) => entry.part);
      const candidateParts = (candidate.structure?.chapters || []).map((entry) => entry.part);
      if (JSON.stringify(candidateParts) !== JSON.stringify(referenceParts)) {
        errors.push(`${locale} chapter part mapping must match ${referenceLocale} 1:1`);
      }
      const referenceSpecial = (reference.structure?.specialPages || []).map((entry) => [entry.id, entry.section, entry.order]);
      const candidateSpecial = (candidate.structure?.specialPages || []).map((entry) => [entry.id, entry.section, entry.order]);
      if (JSON.stringify(candidateSpecial) !== JSON.stringify(referenceSpecial)) {
        errors.push(`${locale} special page ids/sections/orders must match ${referenceLocale} 1:1`);
      }
    }
  }

  return errors;
}

function navigationEntry(entry) {
  const result = {
    id: entry.id,
    title: entry.title,
  };
  if (entry.description) result.description = entry.description;
  result.path = entry.path;
  result.order = entry.order;
  if (entry.part) result.part = entry.part;
  return result;
}

function renderNavigation(editions) {
  const navigation = {};
  for (const [locale, config] of Object.entries(editions)) {
    const localeNavigation = {};
    for (const section of NAVIGATION_SECTIONS) {
      let entries = [];
      if (section === 'chapters' || section === 'appendices') {
        entries = config.structure[section];
      } else {
        entries = config.structure.specialPages.filter((entry) => entry.section === section);
      }
      if (entries.length > 0) {
        localeNavigation[section] = [...entries]
          .sort((left, right) => left.order - right.order)
          .map(navigationEntry);
      }
    }
    navigation[locale] = localeNavigation;
  }
  return navigation;
}

function renderLocales(editions) {
  const locales = {};
  for (const [locale, config] of Object.entries(editions)) {
    locales[locale] = {
      book_title: config.title,
      subtitle: config.subtitle,
      description: config.description,
      language_name: config.ui.languageName,
      alternate_language_name: config.ui.alternateLanguageName,
      labels: config.ui.labels,
      footer_license: config.ui.footerLicense,
    };
  }
  return locales;
}

function renderJekyllConfig(model) {
  const { manifest, publication, editions } = model;
  const defaultLocale = manifest.project.defaultEdition;
  const config = editions[defaultLocale];
  const repository = repositoryIdentity(manifest.repository.url);
  const structureEntry = (entry) => ({
    id: entry.id,
    title: entry.title,
    description: entry.description,
    path: entry.path,
    order: entry.order,
    ...(entry.part ? { part: entry.part } : {}),
  });
  return {
    title: config.title,
    description: config.description,
    author: config.author,
    organization: publication.jekyll.organization,
    version: manifest.project.version,
    lang: config.language,
    default_locale: defaultLocale,
    supported_locales: Object.keys(manifest.editions),
    markdown: publication.jekyll.markdown,
    highlighter: publication.jekyll.highlighter,
    plugins: publication.jekyll.plugins,
    exclude: publication.jekyll.exclude,
    kramdown: publication.jekyll.kramdown,
    url: `https://${repository.owner}.github.io`,
    baseurl: `/${repository.repository}`,
    repository: `${repository.owner}/${repository.repository}`,
    structure: {
      parts: config.structure.parts.map((entry) => ({ ...entry })),
      chapters: config.structure.chapters.map(structureEntry),
      appendices: config.structure.appendices.map(structureEntry),
    },
    defaults: publication.jekyll.defaults,
    permalink: publication.jekyll.permalink,
  };
}

function renderMobileConfig(locale, model) {
  const { manifest, publication, editions } = model;
  const config = editions[locale];
  const otherLocale = Object.keys(manifest.editions).find((candidate) => candidate !== locale);
  return {
    _generated: {
      by: 'scripts/generate-publication-metadata.js',
      sources: ['book-config.json', `book-config.${locale}.json`, 'publication-config.json'],
    },
    edition: {
      id: locale,
      locale: config.edition.locale,
      contentRoot: config.edition.publishRoot,
      languageSwitcherTarget: otherLocale,
    },
    content: {
      parts: config.structure.parts,
      specialPages: config.structure.specialPages,
      chapters: config.structure.chapters,
      appendices: config.structure.appendices,
    },
    performance: publication.mobile.performance,
    navigation: publication.mobile.navigation,
    typography: publication.mobile.typography,
    layout: publication.mobile.layout,
  };
}

function renderTocMain(config) {
  const lines = [`<!-- ${GENERATED_HEADER} -->`, ''];
  const specialPages = [...config.structure.specialPages].sort((left, right) => left.order - right.order);
  for (const page of specialPages.filter((entry) => entry.section === 'introduction' || entry.section === 'additional')) {
    lines.push(`- [${page.title}]({{ '${page.path}' | relative_url }})`);
  }
  for (const part of [...config.structure.parts].sort((left, right) => left.order - right.order)) {
    lines.push('', `### ${part.title}`, '');
    if (part.description) lines.push(`- ${part.description}`);
    for (const chapter of config.structure.chapters.filter((entry) => entry.part === part.id)) {
      lines.push(`- [${chapter.title}]({{ '${chapter.path}' | relative_url }})`);
    }
  }
  return `${lines.join('\n').trimEnd()}\n`;
}

function renderTocAppendices(config) {
  const lines = [`<!-- ${GENERATED_HEADER} -->`, ''];
  for (const appendix of [...config.structure.appendices].sort((left, right) => left.order - right.order)) {
    lines.push(`- [${appendix.title}]({{ '${appendix.path}' | relative_url }})`);
  }
  const afterwordPages = config.structure.specialPages
    .filter((entry) => entry.section === 'afterword')
    .sort((left, right) => left.order - right.order);
  for (const page of afterwordPages) {
    lines.push(`- [${page.title}]({{ '${page.path}' | relative_url }})`);
  }
  return `${lines.join('\n').trimEnd()}\n`;
}

function yamlArtifact(value) {
  return `# ${GENERATED_HEADER}\n${YAML.stringify(value, { lineWidth: 0 })}`;
}

function renderGeneratedArtifacts(model) {
  const errors = validatePublicationModel(model);
  if (errors.length > 0) {
    const error = new Error(`Publication metadata validation failed:\n- ${errors.join('\n- ')}`);
    error.validationErrors = errors;
    throw error;
  }

  const artifacts = new Map();
  artifacts.set('docs/_config.yml', yamlArtifact(renderJekyllConfig(model)));
  artifacts.set('docs/_data/navigation.yml', yamlArtifact(renderNavigation(model.editions)));
  artifacts.set('docs/_data/locales.yml', yamlArtifact(renderLocales(model.editions)));
  for (const [locale, manifestEdition] of Object.entries(model.manifest.editions)) {
    artifacts.set(manifestEdition.mobileConfig, `${JSON.stringify(renderMobileConfig(locale, model), null, 2)}\n`);
    artifacts.set(`docs/_includes/generated/toc-main-${locale}.md`, renderTocMain(model.editions[locale]));
    artifacts.set(`docs/_includes/generated/toc-appendices-${locale}.md`, renderTocAppendices(model.editions[locale]));
  }
  return artifacts;
}

function discoverGeneratedArtifacts(repoRoot) {
  const discovered = new Set();
  if (!fs.existsSync(repoRoot)) return discovered;
  const candidates = ['docs/_config.yml'];
  const dataDirectory = path.join(repoRoot, 'docs', '_data');
  if (fs.existsSync(dataDirectory)) {
    for (const entry of fs.readdirSync(dataDirectory, { withFileTypes: true })) {
      if (entry.isFile()) candidates.push(`docs/_data/${entry.name}`);
    }
  }
  const includeDirectory = path.join(repoRoot, 'docs', '_includes', 'generated');
  if (fs.existsSync(includeDirectory)) {
    for (const entry of fs.readdirSync(includeDirectory, { withFileTypes: true })) {
      candidates.push(`docs/_includes/generated/${entry.name}`);
    }
  }
  for (const entry of fs.readdirSync(repoRoot, { withFileTypes: true })) {
    if (entry.isFile() && /^mobile-config\.[A-Za-z0-9_-]+\.json$/.test(entry.name)) candidates.push(entry.name);
  }

  for (const relativePath of candidates) {
    const candidatePath = resolveWithinRoot(repoRoot, relativePath, `generated artifact candidate ${relativePath}`);
    if (!fs.existsSync(candidatePath)) continue;
    const stat = fs.lstatSync(candidatePath);
    if (!stat.isFile()) {
      if (relativePath.startsWith('docs/_includes/generated/')) discovered.add(relativePath);
      continue;
    }
    if (relativePath.startsWith('docs/_includes/generated/')) {
      discovered.add(relativePath);
      continue;
    }
    const content = fs.readFileSync(candidatePath, 'utf8');
    if (content.startsWith(`# ${GENERATED_HEADER}\n`) || content.startsWith(`<!-- ${GENERATED_HEADER} -->\n`)) {
      discovered.add(relativePath);
      continue;
    }
    if (relativePath.startsWith('mobile-config.')) {
      try {
        const parsed = JSON.parse(content);
        if (parsed?._generated?.by === 'scripts/generate-publication-metadata.js') discovered.add(relativePath);
      } catch {
        // Invalid JSON is reported by the canonical metadata checker when expected.
      }
    }
  }
  return discovered;
}

function writeGeneratedArtifacts(repoRoot, artifacts) {
  const expectedPaths = new Set(artifacts.keys());
  for (const relativePath of discoverGeneratedArtifacts(repoRoot)) {
    if (!expectedPaths.has(relativePath)) {
      fs.rmSync(resolveWithinRoot(repoRoot, relativePath, `orphaned generated artifact ${relativePath}`), {
        recursive: true,
        force: true,
      });
    }
  }
  const includeDirectory = path.join(repoRoot, 'docs', '_includes', 'generated');
  if ([...artifacts.keys()].some((relativePath) => relativePath.startsWith('docs/_includes/generated/'))) {
    fs.rmSync(includeDirectory, { recursive: true, force: true });
  }
  for (const [relativePath, content] of artifacts) {
    const outputPath = resolveWithinRoot(repoRoot, relativePath, `generated artifact ${relativePath}`);
    fs.mkdirSync(path.dirname(outputPath), { recursive: true });
    fs.writeFileSync(outputPath, content);
  }
}

function compareGeneratedArtifacts(repoRoot, artifacts) {
  const differences = [];
  for (const [relativePath, expected] of artifacts) {
    const outputPath = resolveWithinRoot(repoRoot, relativePath, `generated artifact ${relativePath}`);
    if (!fs.existsSync(outputPath)) {
      differences.push(`${relativePath}: missing generated artifact`);
      continue;
    }
    const actual = fs.readFileSync(outputPath, 'utf8');
    if (actual !== expected) differences.push(`${relativePath}: stale or manually edited generated artifact`);
  }
  const expectedPaths = new Set(artifacts.keys());
  for (const relativePath of discoverGeneratedArtifacts(repoRoot)) {
    if (!expectedPaths.has(relativePath)) differences.push(`${relativePath}: unexpected generated artifact`);
  }
  return differences;
}

